const IGNORED_TAGS = new Set(['SCRIPT', 'STYLE', 'NOSCRIPT', 'PRE', 'CODE', 'TEXTAREA'])

const nodeOriginalText = new WeakMap()
const attrOriginalText = new WeakMap()

const normalizeSpaces = (text) => text.replace(/\s+/g, ' ').trim()

const applyCase = (source, translated) => {
  if (!source) return translated
  if (source === source.toUpperCase()) return translated.toUpperCase()
  if (source[0] === source[0].toUpperCase()) return translated[0].toUpperCase() + translated.slice(1)
  return translated
}

const translateWordsEs = (text) =>
  text.replace(/\b([A-Za-z][A-Za-z']*)\b/g, (word) => {
    const lower = word.toLowerCase()
    const mapped = WORD_MAP_ES[lower]
    if (!mapped) return word
    return applyCase(word, mapped)
  })

const translateTextEs = (text) => {
  const normalized = normalizeSpaces(text)
  if (!normalized) return text
  const exact = PHRASE_MAP_ES[normalized]
  if (exact) {
    const leading = text.match(/^\s*/)?.[0] || ''
    const trailing = text.match(/\s*$/)?.[0] || ''
    return `${leading}${exact}${trailing}`
  }
  return translateWordsEs(text)
}

const translateText = (language, text) => {
  if (language === 'es') return translateTextEs(text)
  return text
}

const translateAttributes = (language, root = document.body) => {
  const elements = root.querySelectorAll('[placeholder],[aria-label],[title]')
  elements.forEach((element) => {
    ;['placeholder', 'aria-label', 'title'].forEach((attr) => {
      const current = element.getAttribute(attr)
      if (!current) return
      if (!attrOriginalText.has(element)) attrOriginalText.set(element, {})
      const store = attrOriginalText.get(element)
      if (!store[attr]) store[attr] = current
      const base = store[attr]
      const translated = language === 'en' ? base : translateText(language, base)
      if (current !== translated) {
        element.setAttribute(attr, translated)
      }
    })
  })
}

const translateTextNodes = (language, root = document.body) => {
  const walker = document.createTreeWalker(root, NodeFilter.SHOW_TEXT)
  let node = walker.nextNode()
  while (node) {
    const parent = node.parentElement
    if (!parent || !IGNORED_TAGS.has(parent.tagName)) {
      const value = node.nodeValue || ''
      if (normalizeSpaces(value)) {
        if (!nodeOriginalText.has(node)) nodeOriginalText.set(node, value)
        const base = nodeOriginalText.get(node)
        const translated = language === 'en' ? base : translateText(language, base)
        if (node.nodeValue !== translated) {
          node.nodeValue = translated
        }
      }
    }
    node = walker.nextNode()
  }
}

let activeObserver = null

export const enableRuntimeDomTranslations = (language) => {
  if (typeof document === 'undefined') return () => {}

  const apply = () => {
    translateTextNodes(language)
    translateAttributes(language)
  }

  apply()

  if (activeObserver) activeObserver.disconnect()
  activeObserver = new MutationObserver(() => apply())
  activeObserver.observe(document.body, {
    childList: true,
    subtree: true,
    characterData: true,
    attributes: true,
    attributeFilter: ['placeholder', 'aria-label', 'title']
  })

  return () => {
    if (activeObserver) {
      activeObserver.disconnect()
      activeObserver = null
    }
  }
}
